package week1.day1;

public class LearnDatatypes {

	public static void main(String[] args) {

		// byte
		byte age = 32;

		// short
		short ageOfPerson = 128;

		// int
		int amount = 50000;

		// long
		long mobileNumber = 8925411170L;

		// Float
		float rate1 = 4.25f;

		float rate2 = 1.123456789f;
		System.out.println(rate2);

		double rateOfInterest = 1.123456789;
		System.out.println(rateOfInterest);

		// char
		char logoOfTestleaf = 't';

		// boolean
		boolean doYouKnowJava = true;

		// String
		String name = "Vineeth";

		// print multiple variables
		System.out.println(name + " " + age);
		System.out.println("My name is " + name);

	}

}
