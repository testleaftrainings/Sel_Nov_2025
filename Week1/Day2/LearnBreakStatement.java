package week1.day2;

public class LearnBreakStatement {
	
	//Break statement - To break and comes out of the loop

	public static void main(String[] args) {
		//  1   2   3    4   5
		for (int i = 1; i <=5; i++) {
         // 3==3
         if(i==3) {
        	 break;
         }
			System.out.println(i);
		}

	}

}
