package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		
		//starting point-1
		//ending point-5
		//increment - Increase the number
		
		// To print numbers from 1 to 5
		
		// 1  2  3  4   5
		for(int i=1;i<=5;i++) {
			System.out.println(i);
			}
		
		System.out.println("--------------------");
		
		// To print numbers from 5 to 1
		//   5    4    3     2    1
		//starting point -5
		//ending point -1
		//decreased by 1
		//   5   4   3   2   1
		for (int i=5;i>=5;i--) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
